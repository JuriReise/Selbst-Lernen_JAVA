package Type_Daten;

public class MathOper {
    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        int sum = a + b;           // Сложение
        int difference = a - b;    // Вычитание
        int product = a * b;       // Умножение
        int quotient = a / b;      // Деление
        int remainder = a % b;     // Остаток от деления

        System.out.println("Сложение: " + sum);         // 10 + 3 = 13
        System.out.println("Вычитание: " + difference); // 10 - 3 = 7
        System.out.println("Умножение: " + product);    // 10 * 3 = 30
        System.out.println("Деление: " + quotient);     // 10 / 3 = 3 (целочисленное деление)
        System.out.println("Остаток от деления: " + remainder); // 10 % 3 = 1


    } // end of main


} // end pf class
