package CycleAll;

public class MultiplicationTableWithFOR {
    public static void main(String[] args) {
// Выводим заголовок таблицы
        System.out.println("Таблица умножения от 1 до 10:");

        // Внешний цикл для перебора множителей
        for (int i = 1; i <= 10; i++) {
            // Внутренний цикл для умножения
            for (int j = 1; j <= 10; j++) {
                // Выводим результат умножения
                System.out.print(i * j + "\t"); // \t для табуляции
            }
            // Переход на новую строку после каждой строки таблицы
            System.out.println();

        } // end of loop


    } // end of main


} // end of class
// Внешний цикл ( for (int i = 1; i <= 10; i++)) :
//
//    Перебирает множители от 1 до 10. Каждая итерация соответствует одной строке таблицы.
//
//Внутренний цикл ( for (int j = 1; j <= 10; j++)) :
//
//    Перебирает множители от 1 до 10 для текущего значения i.
//    Каждая итерация внутри внешнего цикла соответствует одному произведению в строке.
//
//System.out.print(i * j + "\t"):
//
//    Вычисляет произведение i и j,
//    выводит результат на экран и добавляет табуляцию для лучшего оформления.
//
//System.out.println():
//
//    После завершения внутреннего цикла (после вывода всех произведений для текущего i) делаем переход на новую строку,
//    чтобы начать вывод следующей строки таблицы.


