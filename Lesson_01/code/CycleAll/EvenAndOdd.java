package CycleAll;
//Напиши программу, которая:
//
//    Выводит все нечётные числа от 1 до 50.
//    Для каждого нечётного числа выводит сообщение в формате: "Число [число] является нечётным."
//
//Затем, расширь программу так, чтобы:
//
//    Считала сумму всех нечётных чисел от 1 до 50.
//    После завершения цикла выводила общую сумму н

public class EvenAndOdd {
    public static void main(String[] args) {
        int sum = 0; // Переменная для хранения суммы нечётных чисел

        for (int i = 0; i <=50; i++) {
            if (i % 2 !=0) { // Проверка на нечётность
                System.out.println("Число: " + i + " является нечётным.");
                sum += i; // Добавляем нечётное число к сумме

            } // end of if
            
        } // end of fori

        // Вывод общей суммы
        System.out.println("Сумма нечётных чисел от 1 до 50: " + sum);
        
    } // end of main



} // end of class

// Как это рабтает:
//    int sum = 0; — мы создаём переменную для хранения суммы нечётных чисел.
//    sum += i; — добавляем текущее нечетное число i к переменной sum внутри блока if.
//    System.out.println("Сумма нечётных чисел от 1 до 50: " + sum); — выводим общую сумму после завершения цикла.
//
//Теперь программа будет выводить все нечётные числа от 1 до 50 и в конце покажет их сумму.

//Что такое +=?
//
//Оператор += — это сокращённая форма для добавления значения к переменной. Он работает так:
//
//    sum += i эквивалентно sum = sum + i.
//
//Как это работает?
//
//    Предположим :
//        Изначально у нас есть переменная sum, которая равна 0.
//        Пусть i равно 3 (например, это нечетное число).
//
//    Когда мы пишем sum += i:
//        Это значит: "Добавь значение i (в нашем случае 3) к sum".
//        Если sum было 0, то теперь оно станет 0 + 3 = 3.
//
//    На следующей итерации :
//        Пусть i равно 5 (следующее нечётное число).
//        Теперь мы выполняем sum += i, что эквивалентно sum = sum + 5.
//        Теперь sum станет 3 + 5 = 8.
//
//Пример
//
//Вот шаги, как это работает в цикле:
//
//    Изначально: sum = 0.
//    1-я итерация : i = 1, sum += 1 → sum = 0 + 1 = 1.
//    2-я итерация : i = 3, sum += 3 → sum = 1 + 3 = 4.
//    3-я итерация : i = 5, sum += 5 → sum = 4 + 5 = 9.
//
//Таким образом, += позволяет легко и быстро обновлять значение переменной, добавляя к ней новое значение.
